    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) newProdTable.getModel();
        String pfields[];
        pfields = getProdFields();
        try
        {
            Class.forName("java.sql.Driver");
            Connection conn = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory","root","");
            Statement st= conn.createStatement();
            String qry = "select pID,pname,quantity,selling_price from product where pID = "+pfields[0]+";";
            ResultSet rs = st.executeQuery(qry);
            if(!rs.next())
                 JOptionPane.showMessageDialog(this,"Item Not found with his id");
            else
            {
                int pid = rs.getInt("pID");
                String name = rs.getString("pname");
                int qty = rs.getInt("quantity");
                float sp = rs.getFloat("selling_price");
                float nc = sp*Integer.parseInt(pfields[2]);
                Total=Total+nc;
                int qt = qty-Integer.parseInt(pfields[2]);
                model.addRow(new Object[]{name,Integer.parseInt(pfields[2]),sp,nc});
                updateS_O_SDBtable(pid,Integer.parseInt(pfields[2]),nc);
                updateP_qty(pid,qt);
            }
            rs.close();
            st.close();
            conn.close();
            totalTF.setText(Float.toString(Total));
            resetNewProdTFs();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
            JOptionPane.showMessageDialog(this,"exception in nextButton");
            e.printStackTrace();
        }
    }//GEN-LAST:event_nextButtonActionPerformed
