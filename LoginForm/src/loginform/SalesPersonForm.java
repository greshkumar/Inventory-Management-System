    public void updateS_O_DBtable(int stid, float tcost)
    {
        try
        {
            Class.forName("java.sql.Driver");
            Connection conn = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory","root","");
            
            if(tcost==0)
            {
                String qry = "insert into sale_order (stID,sale_date,total_cost) values(?,?,?)";
                PreparedStatement pst = conn.prepareStatement(qry);
                pst.setInt(1, stid);
                java.sql.Date  sqlDate = new java.sql.Date(new java.util.Date().getTime());
                pst.setDate(2, sqlDate);
                pst.setFloat(3, 0);
                pst.executeUpdate();
                pst.close();
            }
            else
            {
                String qry = "update sale_order set total_cost ="+tcost+"where stID ="+stid+";";
                Statement st = conn.createStatement();
                int i = st.executeUpdate(qry);
                if(i!=0)
                    JOptionPane.showMessageDialog(this,"Total cost updated.");
                else
                    JOptionPane.showMessageDialog(this,"Total cost is not updated.");
                st.close();
            }
            conn.close();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
            JOptionPane.showMessageDialog(this,"exception in updateP_O_DBtable");
            e.printStackTrace();
        }
    }
